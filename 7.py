# Tuples in python

# Tuples are immutable

# tuples is enclosed in ()

tup=(1,2,3)
print(type(tup))
print(tup)

# indexing

tup=(4,5,6)
print(tup[0])
print(tup[1])
print(tup)

# slicing
tup=(1,2,3)
print(tup[0:2])
print(tup)

# methods

tup=(3,4,56,7)
print(tup.index(4))# returns index of first occurence


tup=(3,4,56,7)
print(tup.count(7))# counts total occurences





